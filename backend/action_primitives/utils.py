"""Utility classes and functions shared across action primitives.

This module centralizes common helpers so interaction builders remain
consistent and easy to reason about.
"""

from __future__ import annotations

import logging
from collections.abc import MutableMapping
from typing import TYPE_CHECKING, Any, Union

from action_primitives.constants import CardSource, SymbolName
from models.board_utils import BoardColorIterator

if TYPE_CHECKING:
    from action_primitives.base import ActionContext
